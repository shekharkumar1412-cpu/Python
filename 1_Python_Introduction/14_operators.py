# Python Arithmetic Operators

a = 7
b = 2

# division
print ('Division: ', a / b) 

# floor division(quotient)
print ('Floor Division: ', a // b)

# modulo(remainder)
print ('Modulo: ', a % b)  

# a to the power b
print ('Power: ', a ** b)   


# Adding two strings
