# computer programmers need to work with binary (base 2), 
# hexadecimal (base 16) and octal (base 8) number systems.

# Number          System Prefix
# Binary	        0b or 0B
# Octal	            0o or 0O
# Hexadecimal	    0x or 0X



print(0b1101011)  # prints 107 (2^0+2^1+2^3+2^5+2^6)

print(0xFB + 0b10)  # prints 253 ((15*16^1+11*16^0)+(1*2^1))

print(0o15)         # # prints 13  (5*8^0+1*8^1)
