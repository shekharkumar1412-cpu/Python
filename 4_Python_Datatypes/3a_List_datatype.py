# In Python, lists are used to store multiple data at once which may be of different types
# A list is created in Python by placing items inside [], separated by commas .

# A list with 3 integers
numbers = [1, 2, 5]

print(numbers)

# empty list
my_list = []


# list with mixed data types
my_list = [1, "Hello", 3.4]
print()
print(my_list)

# indexing starts from 0 similar to array

print('printing elements using index:')
print(my_list[0])
print(my_list[1])
print(my_list[2])
print()


# negative indexing
print('Printing elements using negative indexing')
print(my_list[-1])
print(my_list[-2])
print(my_list[-3])
