# to create a tuple with only one element we need to provide a comma after the element


var1 = ("hello")

# using type() function to know which class a variable or a value belongs to.
print(type(var1))  # <class 'str'>

# Creating a tuple having one element
var2 = ("hello",)
print(type(var2))  # <class 'tuple'>

# Parentheses is optional
var3 = "hello",
print(type(var3))  # <class 'tuple'>