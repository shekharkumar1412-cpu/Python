# There are many operations that can be performed with strings
#  which makes it one of the most used data types in Python.

# 1. Compare Two Strings

str1 = "Hello, world!"
str2 = "I love Python."
str3 = "Hello, world!"

# compare str1 and str2
print(str1 == str2)

# compare str1 and str3
print(str1 == str3)



# Joining(concatenating) two strings using + operator

greet = "Hello, "
name = "Jack"

# using + operator
result = greet + name
print(result)

# Output: Hello, Jack